// MARK: - Helper Functions

private func formatDistance(_ workout: Day) -> String {
    // –°–ø–æ—á–∞—Ç–∫—É –ø–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ –∑–±–µ—Ä–µ–∂–µ–Ω—É –¥–∏—Å—Ç–∞–Ω—Ü—ñ—é
    if let distance = workout.distance, distance > 0 {
        if distance >= 1000 {
            let km = distance / 1000
            // –í–∏–¥–∞–ª—è—î–º–æ –∑–∞–π–≤—ñ –Ω—É–ª—ñ –ø—ñ—Å–ª—è –∫–æ–º–∏
            if km.truncatingRemainder(dividingBy: 1) == 0 {
                return String(format: "%.0f –∫–º", km)
            } else {
                return String(format: "%.1f –∫–º", km)
            }
        } else {
            return String(format: "%.0f –º", distance)
        }
    }
    
    // –Ø–∫—â–æ –Ω–µ–º–∞—î –∑–±–µ—Ä–µ–∂–µ–Ω–æ—ó –¥–∏—Å—Ç–∞–Ω—Ü—ñ—ó, –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ –ø—Ä–∏–±–ª–∏–∑–Ω–∏–π —Ä–æ–∑—Ä–∞—Ö—É–Ω–æ–∫ –Ω–∞ –æ—Å–Ω–æ–≤—ñ –∫—Ä–æ–∫—ñ–≤
    if let steps = workout.steps, workout.sportType.hasSteps {
        let distance = Double(steps) * 0.0008 // –ü—Ä–∏–±–ª–∏–∑–Ω–æ 0.8–º –Ω–∞ –∫—Ä–æ–∫
        if distance >= 1000 {
            let km = distance / 1000
            // –í–∏–¥–∞–ª—è—î–º–æ –∑–∞–π–≤—ñ –Ω—É–ª—ñ –ø—ñ—Å–ª—è –∫–æ–º–∏
            if km.truncatingRemainder(dividingBy: 1) == 0 {
                return String(format: "%.0f –∫–º", km)
            } else {
                return String(format: "%.1f –∫–º", km)
            }
        } else {
            return String(format: "%.0f –º", distance)
        }
    }
    
    return "‚Äî"
}

private func formatStartTime(_ workout: Day) -> String {
    let formatter = DateFormatter()
    formatter.dateStyle = .medium
    formatter.timeStyle = .short
    formatter.locale = Locale(identifier: "uk_UA")
    return formatter.string(from: workout.date)
}

private func formatEndTime(_ workout: Day) -> String {
    let endTime = workout.date.addingTimeInterval(workout.duration)
    let formatter = DateFormatter()
    formatter.dateStyle = .medium
    formatter.timeStyle = .short
    formatter.locale = Locale(identifier: "uk_UA")
    return formatter.string(from: endTime)
}

